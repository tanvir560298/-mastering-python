# This is a simple calculator made with python
print("This calculator can do operations between 2 operands")
a = input("Enter first number: ")
b = input("Enter second number: ")

print("The Addition of " + a + " and " + b + " is: " + str(int(a) + int(b)))
print("The Subtraction of " + a + " and " + b + " is: " + str(int(a) - int(b)))
print("The Multiplication of " + a + " and " + b + " is: " + str(int(a) * int(b)))
print("The Division of " + a + " and " + b + " is: " + str(int(a) / int(b)))
print("The Modulus of " + a + " and " + b + " is: " + str(int(a) % int(b)))
print("The Exponentiation of " + a + " and " + b + " is: " + str(int(a) ** int(b)))





